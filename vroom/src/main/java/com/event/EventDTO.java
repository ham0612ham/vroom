package com.event;

public class EventDTO {
	private long eveNum;
	private String userId;
	private String eveTitle;
	private String eveCont;
	private String eveRegDate;
	private String eveModDate;
	
	public long getEveNum() {
		return eveNum;
	}
	public void setEveNum(long eveNum) {
		this.eveNum = eveNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getEveTitle() {
		return eveTitle;
	}
	public void setEveTitle(String eveTitle) {
		this.eveTitle = eveTitle;
	}
	public String getEveCont() {
		return eveCont;
	}
	public void setEveCont(String eveCont) {
		this.eveCont = eveCont;
	}
	public String getEveRegDate() {
		return eveRegDate;
	}
	public void setEveRegDate(String eveRegDate) {
		this.eveRegDate = eveRegDate;
	}
	public String getEveModDate() {
		return eveModDate;
	}
	public void setEveModDate(String eveModDate) {
		this.eveModDate = eveModDate;
	}
	
	

}
