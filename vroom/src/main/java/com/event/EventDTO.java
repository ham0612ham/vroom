package com.event;

public class EventDTO {
	private long eveNum;
	private String userId;
	private String eveTitle;
	private String eveCont;
	private String eveRegDate;
	private long event;
	private String name;
	private String imageFilename;
	
	public long getEveNum() {
		return eveNum;
	}
	public void setEveNum(long eveNum) {
		this.eveNum = eveNum;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getEveTitle() {
		return eveTitle;
	}
	public void setEveTitle(String eveTitle) {
		this.eveTitle = eveTitle;
	}
	public String getEveCont() {
		return eveCont;
	}
	public void setEveCont(String eveCont) {
		this.eveCont = eveCont;
	}
	public String getEveRegDate() {
		return eveRegDate;
	}
	public void setEveRegDate(String eveRegDate) {
		this.eveRegDate = eveRegDate;
	}
	public long getEvent() {
		return event;
	}
	public void setEvent(long event) {
		this.event = event;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getImageFilename() {
		return imageFilename;
	}
	public void setImageFilename(String imageFilename) {
		this.imageFilename = imageFilename;
	}
	
	


	

}
